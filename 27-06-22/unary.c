#include <stdio.h>
int main()
{
        int a=5, b=10, c=15;
        printf("%d\n", a++);
        printf("%d\n", a--);
        printf("%d\n", ++a);
        printf("%d\n", --a);
        printf("%d\n", b++);
        printf("%d\n", b--);
        printf("%d\n", ++b);
        printf("%d\n", --b);
        printf("%d\n", c++);
        printf("%d\n", c--);
        printf("%d\n", ++c);
        printf("%d\n", --c);

        return 0;
}
